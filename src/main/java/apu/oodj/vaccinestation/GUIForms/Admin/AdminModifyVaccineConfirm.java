    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        try {
            FileHandling.ReplaceById("vaccines", vaccine.getId(), newVaccine.ExportData());
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Failed to modify vaccine, please contact admin!");
            return;
        }

        JOptionPane.showMessageDialog(this, "Vaccine modified successfully!");

        new AdminAddRemoveVaccine(this.admin).setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnGoBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGoBackActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to cancel editing the Vaccine?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            new AdminAddRemoveVaccine(this.admin).setVisible(true);
            this.setVisible(false);
            return;
        }
    }//GEN-LAST:event_btnGoBackActionPerformed

    private void btnModifyAgainActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyAgainActionPerformed
        new AdminModifyVaccine(this.admin, this.vaccine, this.newVaccine).setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnModifyAgainActionPerformed

