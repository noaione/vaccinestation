    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        String name = txtName.getText();
        String manufacturer = txtManufacturer.getText();
        int dosage = (int) txtDosage.getValue();
        String vaccineType = (String) comboVaccineType.getSelectedItem();

        if (name.strip().length() < 1) {
            JOptionPane.showMessageDialog(this, "Please enter a name for the vaccine.");
            return;
        }

        if (manufacturer.strip().length() < 1) {
            JOptionPane.showMessageDialog(this, "Please enter a manufacturer for the vaccine.");
            return;
        }

        // https://stackoverflow.com/questions/5455794/removing-whitespace-from-strings-in-java
        vaccineType = vaccineType.replaceAll("\\s", "");
        VaccineType actualType = VaccineType.fromString(vaccineType);

        Vaccine vaccine = new Vaccine(name, dosage, manufacturer, actualType);
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to add this vaccine?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            FileHandling.WriteString("vaccines", vaccine.ExportData());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Vaccine could not be added!");
            return;
        }

        JOptionPane.showMessageDialog(null, "Vaccine added successfully!");
        new AdminAddRemoveVaccine(this.admin).setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_btnSubmitActionPerformed

