    private void btnRemoveVaccineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveVaccineActionPerformed
        Vaccine sel = lstVaccine.getSelectedValue();
        if (sel == null) {
            JOptionPane.showMessageDialog(this, "Please select a vaccine to remove!");
            return;
        }
        int selIndex = lstVaccine.getSelectedIndex();
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove: " + sel.getName() + "?");
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // Remove from database
        try {
            FileHandling.RemoveById("vaccines", sel.getId());
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Unable to remove vaccine because an error occured!");
            return;
        }

        // Remove from list
        this.vaccines.remove(selIndex);
        JOptionPane.showMessageDialog(this, "Vaccine removed!");
        this.refreshVaccineList();

        // Remove the vaccine type from request.
        String[] vaccinesRequest;
        try {
            vaccinesRequest = FileHandling.ReadFile("vaccinerequest");
        } catch (FileNotFoundException ex) {
            vaccinesRequest = new String[] {};
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Unable to remove vaccine from requests because an error occured!");
            return;
        }

        ArrayList<String> newRequests = new ArrayList<>();
        for (String xd : vaccinesRequest) {
            VaccineRequest parse = VaccineRequest.ParseData(xd);
            if (parse.getVaccine().getId().equals(sel.getId())) {
                // Vac found, skip
                continue;
            }
            newRequests.add(xd);
        }

        try {
            FileHandling.WriteString("vaccinerequest", newRequests, false);
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Unable to save vaccine requests because an error occured!");
        }
    }//GEN-LAST:event_btnRemoveVaccineActionPerformed

