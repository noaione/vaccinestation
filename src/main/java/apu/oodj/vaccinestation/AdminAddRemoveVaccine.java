    public AdminAddRemoveVaccine(Administrator admin) {
        initComponents();
        this.admin = admin;

        String[] rawVaccines;
        try {
            rawVaccines = FileHandling.ReadFile("vaccines");
        } catch (FileNotFoundException ex) {
            rawVaccines = new String[] {};
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to read vaccines information, please contact Admin!");
            new HomepageAdmin(this.admin).setVisible(true);
            this.setVisible(false);
            return;
        }

        this.vaccines = new ArrayList<Vaccine>();
        for (String rawVaccine : rawVaccines) {
            vaccines.add(Vaccine.ParseData(rawVaccine));
        }

        this.refreshVaccineList();
    }

    public void refreshVaccineList() {
        DefaultListModel<Vaccine> model = new DefaultListModel<>();
        for (Vaccine vaccine : vaccines) {
            model.addElement(vaccine);
        }
        lstVaccine.setModel(model);
    }

